						o.CAMBIOREF AS TIPOCAMBIO,
						TRIM(o.VALIDACION) AS VALIDACION,
						TO_CHAR(o.FECHACONTRATACION,'dd/mm/yyyy') AS CONTRATACION,
						TO_CHAR(o.FECHAVALOR,'dd/mm/yyyy') AS VALOR,
