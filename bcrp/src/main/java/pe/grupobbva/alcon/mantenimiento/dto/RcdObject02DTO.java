package pe.grupobbva.alcon.mantenimiento.dto;

public class RcdObject02DTO {
	private String cofigofila;
	private String divisa; //moneda pactada
	private String cuenta;
	private String codigocentral;
	private String contrato;
	private String fechavencimiento;
	private String importe;
	private String cliente;
	public RcdObject02DTO() {}
	public String getCofigofila() {
		return cofigofila;
	}
	public void setCofigofila(String cofigofila) {
		this.cofigofila = cofigofila;
	}
	public String getDivisa() {
		return divisa;
	}
	public void setDivisa(String divisa) {
		this.divisa = divisa;
	}
	public String getCuenta() {
		return cuenta;
	}
	public void setCuenta(String cuenta) {
		this.cuenta = cuenta;
	}
	public String getCodigocentral() {
		return codigocentral;
	}
	public void setCodigocentral(String codigocentral) {
		this.codigocentral = codigocentral;
	}
	public String getContrato() {
		return contrato;
	}
	public void setContrato(String contrato) {
		this.contrato = contrato;
	}
	public String getFechavencimiento() {
		return fechavencimiento;
	}
	public void setFechavencimiento(String fechavencimiento) {
		this.fechavencimiento = fechavencimiento;
	}
	public String getImporte() {
		return importe;
	}
	public void setImporte(String importe) {
		this.importe = importe;
	}
	public String getCliente() {
		return cliente;
	}
	public void setCliente(String cliente) {
		this.cliente = cliente;
	}
	
	
}
